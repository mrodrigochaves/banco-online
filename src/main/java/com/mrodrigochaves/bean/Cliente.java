package com.mrodrigochaves.bean;

public class Cliente {
    
    private Integer id;         
    private String name;           
    private String login;       
    private String password;    
    
    // Getter para o atributo "id"
    public Integer getId() {
        return id;
    }
    
    // Setter para o atributo "id"
    public void setId(Integer id) {
        this.id = id;
    }
    
    // Getter para o atributo "name"
    public String getName() {
        return name;
    }
    
    // Setter para o atributo "name"
    public void setName(String name) {
        this.name = name;
    }
    
    // Getter para o atributo "login"
    public String getLogin() {
        return login;
    }
    
    // Setter para o atributo "login"
    public void setLogin(String login) {
        this.login = login;
    }
    
    // Getter para o atributo "password"
    public String getPassword() {
        return password;
    }
    
    // Setter para o atributo "password"
    public void setPassword(String password) {
        this.password = password;
    }
}
